package com.Level;

public class Switch_Statement {
	public static void main(String[] args) {
		int WaterBottle =2;
		switch(WaterBottle) {
		case 1:
			System.out.println("Pink");
			break;
		case 2:
			System.out.println(" Blue");
			
		case 3:
			System.out.println("green");
			break;
			default:
				System.out.println("not available");
		}
	}

}